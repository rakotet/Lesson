<?php
  // Запрос от пользователя (чаще всего браузер) состоит из двух частей, первая это заголовки, вторая тело запроса, от вет от сервера так же состоит из этих частей

  header('Location: /a.php'); // ф-я которая добавляет заголовок в ответ от сервера, Location это редирект на какую страницу
