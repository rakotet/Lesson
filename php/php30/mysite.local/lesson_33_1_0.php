  header('Content-type: image/jpg'); // заголовок ответа для скачивания картинки пользователем, тип данных
  header('Content-Disposition: attachment; filename="1.jpg"'); // заголовок имени скачиваемого файла пользователем
  readfile('lib/kupon.jpg'); // ф-я считывает файл по переданному пути и переводит его в поток (stream) который отправляет в теле ответа запроса в виде байт, и файл скачивается со страницы

  print_r(getallheaders()); // ф-я возвращает массив всех заголовков которые пришли от клиента
