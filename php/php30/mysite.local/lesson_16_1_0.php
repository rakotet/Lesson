  echo '-----------<br />';

  echo min(1, 4, -6, 0, 9).'<br />'; // ф-я возвращает минимальный переданный параметр, может принимать массив
  echo max(1, 4, -6, 0, 9).'<br />'; // ф-я возвращает максимальный переданный параметр, может принимать массив
  echo min($arr).'<br />';

  echo '-----------<br />';

  echo base_convert('111', 2, 10).'<br />'; //ф-я переводит число в виде строки из системы исчесления во втором аргументе в систему исчесления в третьем аргументе


