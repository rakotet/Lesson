  echo sin($x).'<br />'; // ф-я возвращает синус угла тригонаметрические ф-и работают только с радианами
  echo cos($x).'<br />'; // ф-я возвращает косинус угла 
  echo tan($x).'<br />'; // ф-я возвращает тангенс угла 
  echo (1 / tan($x)).'<br />'; // находим катангенс угла

  echo '-----------<br />';

  echo asin($x).'<br />'; // арксинус
  echo acos($x).'<br />';
  echo atan($x).'<br />';
  echo (M_PI / 2 - atan($x)).'<br />'; //арккатангенс

  echo '-----------<br />';

  $x = 5;

  echo abs($x).'<br />'; // возвращает модуль числа

  echo '-----------<br />';

  $x = 72.153739456;

  echo round($x).'<br />'; // округляет число до знаков после запятой указанных вовтором параметре 
  echo round($x, 2).'<br />';
  echo round($x, -1).'<br />'; // округление до десятков ( до сотен -2 и т.д.)

  echo floor($x).'<br />'; // округляет до ближайшего целого числа которое меньше переданного
